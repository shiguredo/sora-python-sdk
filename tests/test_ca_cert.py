    sendonly = Sendonly(
        signaling_urls,
        channel_id,
        audio=True,
        video=True,
        metadata=metadata,
        ca_cert=pki_goog_ca_cert,
    )
    sendonly.connect(fake_audio=True, fake_video=True)
